# ── 검색 기능 ─────────────────────────────────────────────────────
search = st.text_input("🔍 용어 검색", placeholder="예: 베타, 배당성향, CAGR...")

for category, terms in TERMS.items():
    filtered = [
        t for t in terms
        if not search or search.lower() in t["term"].lower() or search.lower() in t["def"].lower()
    ]
    if not filtered:
        continue

    st.subheader(category)
    for t in filtered:
        with st.expander(f"**{t['term']}**  —  {t['def']}"):
            col_ex, col_guide = st.columns(2)
            with col_ex:
                st.info(f"**예시**\n\n{t['example']}")
            with col_guide:
                st.success(f"**투자 적용**\n\n{t['guide']}")

